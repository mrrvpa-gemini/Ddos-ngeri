def rainbow_text(text):
    colors = ['\033[91m', '\033[93m', '\033[92m', '\033[96m', '\033[95m', '\033[94m']
    for i, char in enumerate(text):
        print(colors[i % len(colors)] + char, end='', flush=True)
    print('\033[0m', end='')

